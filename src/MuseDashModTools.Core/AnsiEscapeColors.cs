namespace MuseDashModTools.Core;

public static class AnsiEscapeColors
{
    public const string Reset = "\e[0m";
    public const string Bold = "\e[1m";

    public const string Black = "\e[30m";
    public const string Red = "\e[31m";
    public const string Green = "\e[32m";
    public const string Yellow = "\e[33m";
    public const string Blue = "\e[34m";
    public const string Magenta = "\e[35m";
    public const string Cyan = "\e[36m";
    public const string White = "\e[37m";

    public const string BrightBlack = "\e[30;1m";
    public const string BrightRed = "\e[31;1m";
    public const string BrightGreen = "\e[32;1m";
    public const string BrightYellow = "\e[33;1m";
    public const string BrightBlue = "\e[34;1m";
    public const string BrightMagenta = "\e[35;1m";
    public const string BrightCyan = "\e[36;1m";
    public const string BrightWhite = "\e[37;1m";
}